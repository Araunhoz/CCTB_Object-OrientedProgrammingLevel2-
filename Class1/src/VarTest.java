
public class VarTest {
	public static void main(String[] args) {
		int iNum = 0;
		char cVal = 'X';
		boolean bVal = false;
		
		System.out.println("--------------");
		System.out.println(iNum);
		System.out.println(cVal);
		System.out.println(bVal);
		
		iNum = 25;
		cVal = 'B';
		bVal = true;
		
		System.out.println("_______________");
		System.out.println(iNum);
		System.out.println(cVal);
		System.out.println(bVal);
	}

}
